    private void tampil_tabel2(){
        model1.getDataVector().removeAllElements();
        model1.fireTableDataChanged();
        jTable3.setDefaultEditor(Object.class, null);
        try {
            sql="select * from soal";
            rs=stat.executeQuery(sql);
            int no=1;
                 while(rs.next()){
                    Object [] o = new Object[7];
                    o[0] = rs.getString("id_soal");
                    o[1] = rs.getString("soal");
                    o[2] = rs.getString("jawaban_benar");
                    o[3] = rs.getString("jawaban_a");
                    o[4] = rs.getString("jawaban_b");
                    o[5] = rs.getString("jawaban_c");
                    o[6] = rs.getString("jawaban_d");
                    model1.addRow(o);
            
                
            }
           
        } catch (Exception e) {
        }
    }
    private void tampil_setting(){
        try {
            sql="select *  from setting limit 1";
            rs=stat.executeQuery(sql);
            if (rs.next()) {
                if (rs.getString("id_setting").equals("1")) {        
                String waktu=rs.getString("waktu_mengerjakan");
                String token=rs.getString("token");
                jTextField10.setText(waktu+" Menit");
                jTextField9.setText(token);
                jLabel21.setText(rs.getString("mapel"));
                }
                
            }
            
        } catch (Exception e) {
        }
    }
    private void tampil_nilai(){
        try {
            sql="SELECT DISTINCT token FROM nilai";
            rs=stat.executeQuery(sql);
            while (rs.next()) {
                jComboBox1.addItem(rs.getString("token"));
            }
        } catch (Exception e) {
        }
    }

