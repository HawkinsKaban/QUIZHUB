    public User getUserInput() {
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        String role = (String) jComboBox1.getSelectedItem();

        switch (role) {
            case "Admin":
                return new Admin(username, password);
            case "Dosen":
                // You should get additional information for Dosen here (e.g., NIP, name)
                return new Dosen(username, password, "nip", "nama");
            case "Mahasiswa":
                // You should get additional information for Mahasiswa here (e.g., NIM, name)
                return new Mahasiswa(username, password, "nim", "nama");
            default:
                throw new IllegalArgumentException("Invalid role");
        }
    }
    
